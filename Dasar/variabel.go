package main

import "fmt"

func main() {
	var name string
	name = "Vika Putri Ariyanti"
	fmt.Println(name)

	name = "Della Oktavia"
	fmt.Println(name)

	var friendName = "Jelly"
	fmt.Println(friendName)

	var age = 38
	fmt.Println(age)

	country := "Indonesia"
	fmt.Println(country)

	country = "Amerika"
	fmt.Println(country)

	var (
		firstName = "Vika"
		lastName  = "Putri"
	)
	fmt.Println(firstName)
	fmt.Println(lastName)
}
